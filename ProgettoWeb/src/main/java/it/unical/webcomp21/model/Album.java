package it.unical.webcomp21.model;

public class Album {
	
	String nome;
	String sfondo;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getSfondo() {
		return sfondo;
	}
	
	public void setSfondo(String sfondo) {
		this.sfondo = sfondo;
	}

}
